<template>
  <div class="second-page">
    <el-button type="primary" @click="goToHomePage">回到菜单</el-button>
    <el-button type="primary" @click="goToHomePage">退出游戏</el-button>
    <!-- Dock栏 -->
    <div class="dock">
      <div class="dock-item" @click="openAppModal(1)">
        <span>📂</span> <!-- 颜文字图标 -->
        <span>App 1</span>
      </div>
      <div class="dock-item" @click="openAppModal(2)">
        <span>📄</span> <!-- 颜文字图标 -->
        <span>App 2</span>
      </div>
      <!-- 根据需要添加更多的dock项 -->
    </div>
    <!-- 动态组件用于显示窗口 -->
    <transition name="modal">
      <div v-if="showModal" class="modal">
        <div class="modal-content">
          <span class="close" @click="closeAppModal">&times;</span>
          <p>这里是应用内容</p>
        </div>
      </div>
    </transition>
  </div>
</template>

<script>
import { useRouter } from 'vue-router';
import { ref } from 'vue';

export default {
  setup() {
    const router = useRouter();
    const showModal = ref(false);
    const currentAppId = ref(null);

    const goToHomePage = () => {
      router.push({ name: 'WelcomePage' });
    };

    const openAppModal = (appId) => {
      currentAppId.value = appId;
      showModal.value = true;
    };

    const closeAppModal = () => {
      showModal.value = false;
    };

    const goToApp = (appId) => {
      // 根据appId跳转到不同的应用页面
      console.log('Go to App', appId);
    };

    return { goToHomePage, goToApp, openAppModal, closeAppModal, showModal };
  },
};
</script>

